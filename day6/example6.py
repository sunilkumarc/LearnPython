# Problem: 
# If salary < 100 -> print 'Low salary'
# If salary >= 100 and salary < 1000 -> print 'Average salary'
# If salary > 1000 -> print 'High salary'

salary = 2000

if salary < 100:
    print('Low Salary')
elif salary >= 100 and salary < 1000:
    print('Average Salary')
else:
    print('High Salary')
